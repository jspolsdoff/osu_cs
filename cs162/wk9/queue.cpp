/**************************************************************************************************
*						constructor
*
***************************************************************************************************/
Queue::Queue()
{
	front = NULL;
	rear = NULL;
}

/**************************************************************************************************
*						destructor
*
***************************************************************************************************/
Queue::~Queue()
{
	clear();
}

/**************************************************************************************************
*						addBack
*
***************************************************************************************************/
void Queue::addBack(int num)
{
	if (isEmpty())
	{
		front = new QueueNode(num);
		rear = front;
	}
	
	else
	{
		rear->next = new QueueNode(num);
		rear = rear->next;
	}
}

