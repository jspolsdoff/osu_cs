#include <iostream>
#include "stack.hpp"

/**************************************************************************************************
*						push
*
***************************************************************************************************/
void Stack::push(int num)
{
	top = new StackNode(num, top);
}

/**************************************************************************************************
*						pop
*
***************************************************************************************************/
void Stack::pop(int &num)
{
		StackNode *temp;
		
		if (isEmpty())
		{
			std::cout << "The stack is empty.\n";
			exit(1);
		}
		
		else
		{
			num = top->value;
			temp = top;
			top = top->next;
			delete temp;
		}
}

/**************************************************************************************************
*						isEmpty
*
***************************************************************************************************/
bool Stack::isEmpty() const
{
	if (!top)
		return true;
	else
		return false;
}

